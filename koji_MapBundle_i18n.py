def default_language():
    locale = QLocale.system().name()
    locale = str(locale)[0:2].lower() if locale is not None else ''
    return 'ja' if locale == 'ja' else 'en'


def current_language():
    language = _session_language or default_language()
    language = str(language).lower() if language is not None else default_language()
    return language if language in TRANSLATIONS else default_language()


def set_language(language):
    global _session_language
    if language not in TRANSLATIONS:
        language = default_language()
    _session_language = language


def tr(key, **kwargs):
    language = current_language()
    text = TRANSLATIONS.get(language, {}).get(key, TRANSLATIONS['ja'].get(key, key))
    return text.format(**kwargs) if kwargs else text
